# Требует явный параметр difficulty (easy/medium/hard) — сложность
# определяется текущим рейтингом темы в PracticeEloProgressManager,
# а не свободной инструкцией "от простого к сложному".

PRACTICE_QUESTION_PROMPT = PromptTemplate.from_template( # генерация практической задачи с кодом фиксированной сложности
"""
Тема:
{topic}

Заметка пользователя:
{note}


Сгенерируй {num_questions} вопросов уровня сложности: {difficulty}.

Требования к сложности:

- задача должна явно требовать написания кода, а не теоретического объяснения
- если тема предполагает выполнение кода (Python, Pandas), явно укажи в условии задачи,
  что итоговый результат нужно вывести через print()
- без больших проектов, задачи должны решаться в несколько строк кода
- если сложность easy — Простая задача из самых базовых навыков, решается в парочку строк, а порой и в одну
- если сложность medium — вопрос уже большего характера, к примеру решить небольшую задачу на реальных данных которых ты подашь, решается сложнее и требует больших навыков понимания
- если сложность hard — вопрос должен требовать глубокого понимания, разбора нюансов,
  пограничных случаев или связи с другими темами. Подача реальных кейсов задач на решение.

- НЕ генерируй вопрос смешанной или произвольной сложности — строго придерживайся уровня {difficulty}

Общие требования:

- можно немного дополнять материал
- не выходить далеко за пределы темы
- без больших проектов
- вопросы по ML и аналитике
"""
)


